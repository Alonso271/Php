<?php
function sumar($n1, $n2, &$resu) {
    $resu = $n1 + $n2;
}
function resta($n1, $n2, &$resu) {
    $resu = $n1 - $n2;
}
function division($n1, $n2, &$resu) {
    $resu = $n1 / $n2;
}
function multiplicacion($n1, $n2, &$resu) {
    $resu = $n1 * $n2;
}
function potencia($n1, $n2, &$resu) {
    $resu = $n1 ** $n2;
}